char exampleChar = 'a'; // 16-Bit unicode character ; U + 0000 to U + FFFF
double exampleDouble = 14.45533d; // 64-bit double-precision floating point type 
decimal exampleDecimal = .2892532962m; // 128-bit precise decimal values
