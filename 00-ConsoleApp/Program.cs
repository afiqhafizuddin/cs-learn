short exampleShort = -32768; // 16-bit signed interger type
uint exampleUint = 4294967295; // 32-bit signed interger type
ulong exampleULong = 8446744073709551615; // 64-bit unsigned interger type
ushort exampleUShort = 65535; // 16-bit unsigned interger type

